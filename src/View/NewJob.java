    private void butnSaveMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butnSaveMousePressed

        if (JOptionPane.showConfirmDialog(null, "Are you sure you want save new job ?", "Message", JOptionPane.YES_NO_OPTION) == 0) {

            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Item details are empty.", "Empty Taable", JOptionPane.WARNING_MESSAGE);
            } else {

                String jobID = txtJobId.getText();
                String cientCode = txtClientCode.getText();
                String jobDate = dateFormat.format(dcJobDate.getDate());
                String billingMonth = txtBillingMonth.getText();
                String taxNo = txtTaxNo.getText();
                double subTotalVal = Double.parseDouble(txtSubTotal.getText());
                double discount = Double.parseDouble(txtDiscount.getText());
                double netTotal = Double.parseDouble(txtNet.getText());
                int jobStatus = 1;

                Job job = new Job(jobID, cientCode, jobDate, billingMonth, taxNo, subTotalVal, discount, netTotal, jobStatus);
                DBManager dbManager = new DBManager();

                boolean flag = dbManager.InsertNewJob(job);

                if (flag) {

                    int rowCount = 0;

                    for (int count = 0; count < model.getRowCount(); count++) {

                        String productType = model.getValueAt(count, 0).toString();
                        String productDesc = model.getValueAt(count, 1).toString();
                        double sqFeetArea = Double.parseDouble(model.getValueAt(count, 2).toString());
                        double rate = Double.parseDouble(model.getValueAt(count, 3).toString());
                        int units = Integer.parseInt(model.getValueAt(count, 4).toString());
                        String fromDate = model.getValueAt(count, 5).toString();
                        String toDate = model.getValueAt(count, 6).toString();
                        int days = Integer.parseInt(model.getValueAt(count, 7).toString());
                        double itemTotal = Double.parseDouble(model.getValueAt(count, 8).toString());

                        Job itemJob = new Job(jobID, productType, productDesc, sqFeetArea, units, rate, fromDate, toDate, days, itemTotal);
                        DBManager itemDBManager = new DBManager();
                        itemDBManager.insertNewItem(itemJob);

                        rowCount++;
                    }

                    if (rowCount == model.getRowCount()) {

                        JOptionPane.showMessageDialog(null, "Storage job insert successfully.");
                        //  cllearFields();

                        if (JOptionPane.showConfirmDialog(null, "You want to preview invoice?", "Information", JOptionPane.YES_NO_OPTION) == 0) {

                            Invoice invoice = new Invoice();

                            invoice.setJobId(job.getJobId());

                            ViewInvoice viewInvoice = new ViewInvoice(invoice);
                            viewInvoice.setVisible(true);
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Insert Fail.", "Error!", JOptionPane.ERROR_MESSAGE);
                    }

                }

            }
        }

    }//GEN-LAST:event_butnSaveMousePressed

    private void lblAddRowButnMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAddRowButnMousePressed

        if (comProductCategory.getSelectedItem().equals("******** SELECT CATEGORY ********")) {
            JOptionPane.showMessageDialog(null, "Can't add item details. Product category is invalid", "Invalid", JOptionPane.WARNING_MESSAGE);
        } else if (comArea.getSelectedItem().equals("***** SELECT *****")) {
            JOptionPane.showMessageDialog(null, "Can't add item details. invalid squre feet area", "Invalid", JOptionPane.WARNING_MESSAGE);
        } else {

            DBManager dBManager = new DBManager();

            int numberOfDays = dBManager.getNumberOfDays((int) dcFromtDate.getDate().getTime(), (int) dcToDate.getDate().getTime());

            double rate = dBManager.getSqFtArea(Double.parseDouble(comArea.getSelectedItem().toString()));

            int units = (int) spUnits.getValue();

            double unitTotal = rate * units * numberOfDays;

            subTotal += unitTotal;
            txtSubTotal.setText(subTotal + "");

            Object colum[] = new Object[9];

            colum[0] = txtDescription.getText();
            colum[1] = comProductCategory.getSelectedItem();
            colum[2] = comArea.getSelectedItem();
            colum[3] = rate;
            colum[4] = units;
            colum[5] = dateFormat.format(dcFromtDate.getDate());
            colum[6] = dateFormat.format(dcToDate.getDate());
            colum[7] = numberOfDays;
            colum[8] = unitTotal;

            model.addRow(colum);

            txtDescription.setText("");
            comProductCategory.setSelectedItem("******** SELECT CATEGORY ********");
            comArea.setSelectedItem("***** SELECT *****");
            spUnits.setValue(1);
            dcFromtDate.setDate(date);
            dcToDate.setDate(date);
        }
    }//GEN-LAST:event_lblAddRowButnMousePressed

