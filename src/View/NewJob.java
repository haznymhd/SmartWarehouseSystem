    private void lblButnCalculateMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblButnCalculateMousePressed

        if (checkDiscountAmount.isSelected() || checkDiscountRate.isSelected()) {

            double discountAmount = 0.0, netAmount = 0.0, subTotalVal = 0.0, discountRate = 0.0;

            subTotalVal = Double.parseDouble(txtSubTotal.getText());

            if (checkDiscountAmount.isSelected() && !txtDicountAcount.getText().isEmpty()) {

                discountAmount = Double.parseDouble(txtDicountAcount.getText());
                netAmount = subTotalVal - discountAmount;

            } else if (checkDiscountRate.isSelected() && !txtDiscountRate.getText().isEmpty()) {

                discountRate = Double.parseDouble(txtDiscountRate.getText());

                discountAmount = (subTotalVal * discountRate) / 100;
                netAmount = subTotalVal - discountAmount;

            }

            txtDiscount.setText(discountAmount + "");
            txtNet.setText(netAmount + "");
        }

    }//GEN-LAST:event_lblButnCalculateMousePressed

