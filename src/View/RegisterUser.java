    private void butnSaveMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butnSaveMousePressed

        String empNo = txtEmpNo.getText();
        String fName = txtFirstName.getText();
        String lName = txtLastName.getText();
        String mobile = txtMobileNo.getText();
        String email = txtEmail.getText();
        String username = txtUserName.getText();
        String pass = txtPassword.getText();
        String userRole = (String) comUserRole.getSelectedItem();
        int status = 1;
        if (empNo.equals("")) {
            JOptionPane.showMessageDialog(null, "Employee ID can't be empty.", "Empty Field Error", JOptionPane.INFORMATION_MESSAGE);
        } else if (username.equals("") && pass.equals("")) {
            JOptionPane.showMessageDialog(null, "Username & password can't be empty.", "Empty Field Error", JOptionPane.INFORMATION_MESSAGE);
        } else if (userRole.equals("******* User Role *******")) {
            JOptionPane.showMessageDialog(null, "User role is invalid.", "Invalid", JOptionPane.INFORMATION_MESSAGE);
        } else if (!pass.equals(txtConfirm.getText())) {
            AllMarkUnvisible();
            CrossMarkVisible();
            JOptionPane.showMessageDialog(null, "Those passwords didnâ€™t match. Try again.", "Mismatch Error", JOptionPane.WARNING_MESSAGE);
        } else {

            AllMarkUnvisible();
            CheckMarkVisible();

            DBManager dbManager = new DBManager();
            
            User checkUser = new User(empNo);

            boolean checkExsistence = dbManager.checkUserExistence(checkUser);

            if (checkExsistence) {
                JOptionPane.showMessageDialog(null, "Can't save the user information. ( " + empNo + " ) This employee number is aready existing.", "Error!", JOptionPane.WARNING_MESSAGE);
            } else {

                User user = new User(empNo, fName, lName, mobile, email, username, pass, userRole, status);

                boolean flag = dbManager.insertNewUser(user);
                if (flag) {
                    JOptionPane.showMessageDialog(null, "User successfully inserted.", "Sucees", JOptionPane.INFORMATION_MESSAGE);
                    clearFields();
                } else {
                    JOptionPane.showMessageDialog(null, "User insert fail.", "Error!", JOptionPane.ERROR_MESSAGE);
                }
            }

        }
    }//GEN-LAST:event_butnSaveMousePressed

