    private void butnUpdateMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butnUpdateMousePressed

        if (JOptionPane.showConfirmDialog(null, "Are you sure you want update user ?", "Message", JOptionPane.YES_NO_OPTION) == 0) {

            String empNo = txtEmpNo.getText();
            String fName = txtFirstName.getText();
            String lName = txtLastName.getText();
            String mobile = txtMobileNo.getText();
            String email = txtEmail.getText();
            String username = txtUserName.getText();
            String pass = txtPassword.getText();
            String userRole = (String) comUserRole.getSelectedItem();
            int status = 1;

            if (empNo.equals("")) {
                JOptionPane.showMessageDialog(null, "Employee ID can't be empty.", "Empty Field Error!", JOptionPane.INFORMATION_MESSAGE);
            } else if (username.equals("") && pass.equals("")) {
                JOptionPane.showMessageDialog(null, "Username & password can't be empty.", "Empty Field Error!", JOptionPane.INFORMATION_MESSAGE);
            } else if (userRole.equals("******* User Role *******")) {
                JOptionPane.showMessageDialog(null, "User role is invalid.", "Invalid!", JOptionPane.INFORMATION_MESSAGE);
            } else if (!pass.equals(txtConfirm.getText())) {
                AllMarkUnvisible();
                CrossMarkVisible();
                JOptionPane.showMessageDialog(null, "Those passwords didn’t match. Try again.", "Mismatch Error", JOptionPane.WARNING_MESSAGE);
            } else {

                AllMarkUnvisible();
                CheckMarkVisible();

                User user = new User(empNo, fName, lName, mobile, email, username, pass, userRole, status);
                DBManager dBManager = new DBManager();

                boolean flag = dBManager.updateUser(user);

                if (flag) {

                    JOptionPane.showMessageDialog(null, "User update successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                    clearFields();
                } else {
                    JOptionPane.showMessageDialog(null, "User update fail.", "Error!", JOptionPane.ERROR_MESSAGE);
                }
            }

        }
    }//GEN-LAST:event_butnUpdateMousePressed

