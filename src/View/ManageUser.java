    private void butnRemoveMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_butnRemoveMousePressed

        if (JOptionPane.showConfirmDialog(null, "Are you sure you want remove user ?", "Message", JOptionPane.YES_NO_OPTION) == 0) {

            String empNo = txtEmpNo.getText();
            User user = new User(empNo);
            DBManager dbManager = new DBManager();
            boolean flag = dbManager.removeUser(user);

            if (flag) {

                JOptionPane.showMessageDialog(null, "User removed successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                txtEmpNo.setText("");
                txtFirstName.setText("");
                txtLastName.setText("");
                txtMobileNo.setText("");
                txtEmail.setText("");
                txtUserName.setText("");
                txtPassword.setText("");
                comUserRole.setSelectedItem("******* User Role *******");
                txtConfirm.setText("");

            } else {
                JOptionPane.showMessageDialog(null, "User removing fail.", "Error!", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_butnRemoveMousePressed

