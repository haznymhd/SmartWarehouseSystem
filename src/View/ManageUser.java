    private void employeeSearch(String empNo) {
        User user = new User(empNo);
        DBManager dbManager = new DBManager();
        User searchUser = dbManager.searchUser(user);

        clearFieldsWithoutEmployeeID();

        if (empNo.equals("")) {
            JOptionPane.showMessageDialog(null, "Can't be search. Employee Number is empty.", "Empty Field", JOptionPane.INFORMATION_MESSAGE);
        } else if (searchUser == null) {

            JOptionPane.showMessageDialog(null, "Invalid Employee Number", "Invalid", JOptionPane.WARNING_MESSAGE);
        } else {

            txtEmpNo.setText(searchUser.getEmpId());
            txtFirstName.setText(searchUser.getfName());
            txtLastName.setText(searchUser.getlName());
            txtMobileNo.setText(searchUser.getMobileNo());
            txtEmail.setText(searchUser.getEmail());
            txtUserName.setText(searchUser.getUsername());
            txtPassword.setText(searchUser.getPassword());
            comUserRole.setSelectedItem(searchUser.getUserRole());
            txtConfirm.setText(searchUser.getPassword());
        }
    }
